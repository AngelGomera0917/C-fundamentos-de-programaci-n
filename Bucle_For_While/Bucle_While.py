print(" Bucle While, donde me aumentara de 10 en 10 hasta llegar a 150. ")
print("\n")

i = 10    # Variable i igual a 10

while i <= 150: # Mientras i sea menor o igual a 150
    print(i, "\n") # Imprimir i
    i += 10 # Aumentar i de 10 en 10
    
print("\n")


# Explicacion:
# En este caso, el bucle while se ejecutara mientras la condicion sea verdadera.
# En este caso, la condicion es que i sea menor o igual a 150.
# La variable i se inicializa en 10, y se incrementa en 10 en cada iteracion.
# El bucle se ejecutara hasta que i sea menor o igual a 150.

print("\n")


